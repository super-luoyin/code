# while循环结构
"""语法结构
while 条件表达式:
    条件执行体（循环体）
"""
"""
四步循环法:
1.初始化变量
2.条件判断
3.条件执行体（循环体）
4.改变变量
"""
# 1.初始化变量
a = 1
num = 0
# 2.条件判断
while a <= 100:
    # 3.条件执行体
    num += a
    # 4.改变变量
    a += 1
print(num)

# 求偶数和
x = 1
y = 0
while x <= 100:
    if x % 2 == 0:
        y += x
    x += 1
print(y)
